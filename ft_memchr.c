#include "libft.h"

void		*ft_memchr(const void *s, int c, size_t n)
{
	char	*p;

	p = (char *)s;
	while (n)
	{
		if (*p == c)
			return ((void *)p);
		++p;
		--n;
	}
	return (NULL);
}
